public class Martix5 {
	public static void main(String[] args) {
		int k = 0;
		for (int i = 0; i < 5; i++) {
			for (int j = 0; j < 5; j++) {
				k = k+1;
				System.out.print(k+"\t");
			}
			System.out.println("");
		}
	}
}
